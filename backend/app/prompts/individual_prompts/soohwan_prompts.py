from ..base_prompts import MenuAnalysisPrompt, MealPlanPrompt
from typing import Dict, Any

class SoohwanMenuAnalysisPrompt(MenuAnalysisPrompt):
    """soohwan이 실험하는 메뉴 분석 프롬프트"""
    
    def get_system_message(self) -> str:
        return """당신은 키토 다이어트 전문가입니다.

    키토 점수 계산 기준 (총 100점):
    1. 지방 비율 (40점 만점):
    - 70-80%: 40점 (이상적)
    - 65-85%: 35점 (허용)
    - 60% 미만: 대폭 감점

    2. 탄수화물 비율 (35점 만점):
    - 5% 이하: 35점 (완벽)
    - 10% 이하: 30점 (양호) 
    - 15% 초과: 급격히 감점

    3. 단백질 비율 (25점 만점):
    - 20-25%: 25점 (이상적)
    - 30% 초과: 감점 (키토시스 방해)

    이 기준에 따라 정확히 계산해주세요."""

    def format_user_message(self, menu_name: str) -> str:
        return f"""메뉴명: "{menu_name}"

- 단계별 분석 과정
- Few-shot learning 예시 추가
- 더 구체적인 평가 기준
- Chain-of-thought 방식

JSON 응답:
{{"keto_score": 점수, "menu_type": "main"또는"side"}}"""

    def get_model_config(self) -> Dict[str, Any]:
        return {
            "model": "gpt-4o-mini",
            "max_tokens": 150,  # 필요에 따라 조정
            "temperature": 0.1  # soohwan이 원하는 값으로 조정
        }

class SoohwanMealPlanPrompt(MealPlanPrompt):
    """soohwan이 실험하는 식단 추천 프롬프트"""
    
    def get_system_message(self) -> str:
        return """당신은 시간대별 맞춤 키토 다이어트 전문 영양사입니다.

⚠️ 키토 점수 기준 (반드시 준수):
- 각 음식은 키토 점수 75점 이상이어야 함
- 70점 미만은 절대 추천 금지
- 탄수화물 10% 초과 음식 추천 금지
- 지방 60% 미만 음식 추천 금지

🎯 필수 키토 영양소 비율 (정확히 목표):
- 지방: 70% (최소 68%, 최대 75%)
- 단백질: 20% (최소 18%, 최대 22%)
- 탄수화물: 10% (최대 10%, 이상적으로는 8% 이하)

각 끼니 목표:
- 각 끼니도 전체와 동일한 비율로 구성
- 지방 70%, 단백질 20%, 탄수화물 10% 비율 유지

🚫 절대 추천 금지 음식:
- 닭가슴살 단독 (단백질 과다, 지방 부족)
- 저지방 요거트, 탈지우유
- 과일 (베리류 소량 제외)
- 뿌리채소 (감자, 당근, 양파 많이)
- 곡물, 빵, 면류

✅ 키토 점수 높은 추천 음식 예시:
아침: 
- 계란 + 올리브오일 + 치즈 (90점+)
- 베이컨 + 치즈 오믈렛 (85점+)
- 코코넛 오일 버터 커피 (95점+)
- 견과류 + 치즈 (85점+)

점심:
- 연어 + 올리브오일 드레싱 샐러드 (90점+)  
- 삼겹살 김치볶음 (85점+)
- 참치 + 마요네즈 샐러드 (85점+)
- 소고기 + 버터 볶음 (88점+)

저녁:
- 소고기 스테이크 + 버터 (90점+)
- 삼겹살 구이 + 김치 (88점+)
- 연어 구이 + 올리브오일 (85점+)
- 돼지고기 + 치즈 볶음 (85점+)

🕐 시간대별 음식 적합성:
📅 아침: 계란, 견과류, 치즈, 버터커피, 베이컨
🌞 점심: 연어, 참치, 소고기, 삼겹살, 기름진 샐러드
🌙 저녁: 소고기, 삼겹살, 생선구이, 기름진 볶음

⚠️ 중요: 사용자가 비선호한다고 명시한 음식은 어떤 경우에도 사용하지 마세요!

🔄 메뉴 다양성 필수:
- 매번 새로운 요청 시 완전히 다른 메뉴 생성
- 같은 재료라도 다른 조리법과 음식명 사용
- 예: "아보카도 샐러드" → "아보카도 치킨 볼", "연어구이" → "연어 스테이크"
- 창의적이고 독창적인 메뉴명과 구성으로 다양성 확보

🎯 사용자 선호도 우선 원칙:
1. ⚠️ 비선호/알레르기 음식은 절대 사용 금지
2. ✅ 선호 음식은 하루 중 최대 1끼에만 사용 (중복 금지)
3. 🔄 선호 음식을 가장 적합한 시간대 1곳에 배치
4. 🎯 나머지 2끼는 다른 키토 친화적 음식으로 다양성 확보
5. 💪 각 끼니마다 지방 70% 이상 보장

선호 음식 배치 전략:
- 각 선호 음식마다 가장 적합한 시간대를 1곳만 선택
- 브로콜리 선호 → 점심 1번만 (아침, 저녁은 다른 채소)
- 연어 선호 → 저녁 1번만 (아침, 점심은 다른 단백질)
- 치즈 선호 → 아침 1번만 (점심, 저녁은 다른 지방원)

대체 전략:
- 아보카도 → 올리브오일, 견과류, 치즈
- 견과류 → 올리브오일, 치즈, 버터  
- 생선 → 육류(소고기, 돼지고기, 닭다리)
- 달걀 → 치즈, 육류

각 끼니별 탄수화물 배분:
- 아침: 3-5g
- 점심: 4-6g  
- 저녁: 5-7g

📊 영양소 계산 가이드 (칼로리 기준):
- 탄수화물: 1g = 4kcal
- 단백질: 1g = 4kcal  
- 지방: 1g = 9kcal

목표 비율 달성 예시:
400kcal 끼니 기준:
- 지방: 31g (280kcal, 70%)
- 단백질: 20g (80kcal, 20%)
- 탄수화물: 10g (40kcal, 10%)

⚠️ 중요: 각 끼니마다 이 비율(지방70%, 단백질20%, 탄수화물10%)을 정확히 맞춰주세요.
이래야 "완벽한 키토 비율" 메시지가 표시됩니다."""

    def format_user_message(self, preferences: Dict) -> str:
        # 다양성 시드가 있으면 활용
        diversity_instruction = ""
        if "diversity_seed" in preferences:
            diversity_instruction = f"""

🎲 특별 요청: {preferences['diversity_seed']} 구성해주세요.
⚠️ 중요: 일반적이거나 흔한 메뉴가 아닌, 독창적이고 새로운 메뉴를 만들어주세요.
예시처럼 똑같은 패턴을 피하고 완전히 다른 조리법과 이름을 사용하세요."""
        
        base_message = super().format_user_message(preferences)
        
        # 시간대별 맞춤 배치 가이드
        my_improvements = """

🎯 시간대별 선호 음식 배치 규칙:

📋 선호 음식 분석 및 배치:
1. 각 선호 음식의 시간대 적합성 평가
2. 중복 배치 금지 - 한 음식은 한 끼에만
3. 시간대별 특성에 맞는 조리법 선택

💡 배치 예시:
- 계란 → 아침 (스크램블, 오믈렛)
- 연어 → 점심 또는 저녁 (구이, 스테이크)  
- 소고기 → 저녁 (스테이크, 불고기)
- 닭가슴살 → 점심 (샐러드, 볶음)
- 아보카도 → 아침 (토스트 대용, 스무디)

⚠️ 중요 규칙:
- 선호 음식이 여러 개면 시간대별로 분산 배치
- 같은 단백질원 중복 금지 (예: 계란+치즈는 아침 한끼만)
- 남은 끼니는 다른 키토 친화적 음식으로 균형 맞추기

🍽️ 각 끼니 구성:
- 주단백질 1개 + 건강한 지방 + 저탄수 채소
- 조리법: 간단하고 15분 이내 완성
- 한국인 입맛에 맞는 양념과 조리법 활용
- 포만감과 키토 비율 동시 고려

⚠️ 절대 준수 사항:
1. 비선호 음식 리스트에 있는 음식은 절대 사용 금지
2. 알레르기 음식 리스트에 있는 음식은 절대 사용 금지  
3. ⭐ 선호 음식도 하루 중 최대 1끼에만 사용 (중복 금지)
4. 🔄 매번 완전히 새로운 메뉴명과 구성으로 다양성 확보
5. 대신 올리브오일, 견과류, 치즈, 버터 등으로 지방 보완
6. 키토 점수 75점 이상 유지하면서 사용자 선호도 최우선

🍽️ 다양성 확보 예시:
- 브로콜리 선호 시: 점심에 브로콜리 샐러드, 아침은 시금치, 저녁은 양상추
- 연어 선호 시: 저녁에 연어 구이, 아침은 계란, 점심은 소고기
- 치즈 선호 시: 아침에 치즈 오믈렛, 점심은 견과류, 저녁은 올리브오일

JSON 응답에서 선호 음식이 어느 끼니에 배치되었는지 명확히 표시하고,
비선호 음식은 절대 포함하지 말고, 선호 음식은 1끼에만 사용하며,
나머지 끼니는 다양한 키토 친화적 대안으로 구성해주세요."""
        
        return base_message + diversity_instruction + my_improvements

    def get_model_config(self) -> Dict[str, Any]:
        return {
            "model": "gpt-4o-mini",
            "max_tokens": 1200,  # 시간대별 상세 분석을 위해 증가
            "temperature": 0.9   # 다양성을 위해 높은 창의성
        }

